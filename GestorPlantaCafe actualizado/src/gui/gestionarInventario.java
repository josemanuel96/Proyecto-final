    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String insertarSQL = "INSERT INTO plantas (nombre_planta, variedad, estado, cantidad) VALUES (?,?,?,?)";
        try {
            PreparedStatement ps;
            ps = (PreparedStatement) cn.prepareStatement(insertarSQL);
            ps.setString(1, comboNombrePlanta.getSelectedItem().toString());
            ps.setString(2, comboVariedad.getSelectedItem().toString());
            ps.setString(3, comboEstado.getSelectedItem().toString());
            ps.setString(4, txtCantidad.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Registro realizado con exito.");
            btnAgregar.setEnabled(true);
            limpiarCampos();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al tratar de insertar los datos: " + ex);
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

