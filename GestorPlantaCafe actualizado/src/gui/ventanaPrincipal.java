    public void mostrarDatos(String buscarValor) {
        String consultaSQL = "SELECT *FROM Plantas";
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id");
        modelo.addColumn("Nombre_Planta");
        modelo.addColumn("Variedad");
        modelo.addColumn("Estado");
        modelo.addColumn("Cantidad");
        tablaregistro.setModel(modelo);
        //String consultaSQL = "SELECT * FROM empleados ORDER BY id";
        String data[] = new String[5];
        Statement st;
        try {
            st = (Statement) cn.createStatement();
            ResultSet rs = st.executeQuery(consultaSQL);
            while (rs.next()) {
                data[0] = rs.getString(1);
                data[1] = rs.getString(2);
                data[2] = rs.getString(3);
                data[3] = rs.getString(4);
                data[4] = rs.getString(5);
                modelo.addRow(data);
            }
            tablaregistro.setModel(modelo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error en la consulta SQL: " + ex);
        }

    }

